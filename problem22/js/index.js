// write a java script program to get number from the user , then get the square of the number , and at the end get the number again by using the square root to get the number =>
var number = Number(window.prompt("Please Enter The Number Is = "))
console.log("The Number Is = ",number)
var squareOfNumber = number * number 
console.log("The Square Of The Number Is = ",squareOfNumber)
var squareRootOfNumber = Math.sqrt(squareOfNumber)
console.log("The Square Root Of The Number Is = ",squareRootOfNumber)